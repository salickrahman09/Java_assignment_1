public class assignment_ope {
    public static void main(String[] args) {

        int x = 10;
        int y = 5;

        // Simple assignment
        x = y;
        System.out.println("x = y: " + x);

        // Add and assign
        x += y;
        System.out.println("x += y: " + x);

        // Subtract and assign
        x -= y;
        System.out.println("x -= y: " + x);

        // Multiply and assign
        x *= y;
        System.out.println("x *= y: " + x);

        // Divide and assign
        x /= y;
        System.out.println("x /= y: " + x);

        // Modulus and assign
        x %= y;
        System.out.println("x %= y: " + x);

    }
}
