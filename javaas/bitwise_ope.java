public class bitwise_ope {
    public static void main(String[] args) { 
        int a = 42;   
              int b = 25;   
              // Bitwise AND 
              System.out.println("a & b = " + (a & b));  
       
              // Bitwise OR 
              System.out.println("a | b = " + (a | b));  
       
              // Bitwise XOR 
              System.out.println("a ^ b = " + (a ^ b));  
       
              // Bitwise NOT 
              System.out.println("~a = " + (~a));  
       
              // Bitwise Shift Left 
              System.out.println("a << 2 = " + (a << 2));  
       
              // Bitwise Shift Right 
              System.out.println("a >> 2 = " + (a >> 2));  
       
              // Bitwise Unsigned Shift Right 
              System.out.println("a >>> 2 = " + (a >>> 2));  
       
       }
}
